    private void listarVentasConDeuda() {
        modelo.setRowCount(0);

        for (Venta v : ctrlVentas.getVentas()) {
            Pago pago = ctrlPagos.buscarPagoPorVenta(v);

            double saldo = (pago != null) ? pago.getSaldoPendiente() : v.getCotizacion().getMontoTotal();

            if (saldo > 0) {
                Object[] row = {
                    v.getCotizacion().getCliente().getNombres() + " " + v.getCotizacion().getCliente().getApellidoPaterno(),
                    v.getCotizacion().getVehiculo().getMarca() + " " + v.getCotizacion().getVehiculo().getModelo(),
                    String.format("%.2f", v.getCotizacion().getMontoTotal()),
                    String.format("%.2f", saldo)
                };
                modelo.addRow(row);
            }
        }
    }

    private void actualizarLabelSeleccion() {
        int fila = ventasTable.getSelectedRow();
        if (fila >= 0) {
            String saldo = modelo.getValueAt(fila, 3).toString();
            jLabel8.setText("Saldo pendiente: S/ " + saldo);
        }
    }

