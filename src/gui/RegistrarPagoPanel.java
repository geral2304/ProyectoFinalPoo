    private void pagoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pagoButtonActionPerformed
        int selectedRow = ventasTable.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una venta de la tabla.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String montoTexto = montoField.getText();

        if (montoTexto.isEmpty() || !montoTexto.matches("\\d+(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(this, "Ingrese un monto válido.", "Error de formato", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Venta ventaSeleccionada = null;
        int indiceFila = 0;

        for (Venta venta : ctrlVentas.getVentas()) {
            Pago pago = ctrlPagos.buscarPagoPorVenta(venta);
            double saldo = (pago != null) ? pago.getSaldoPendiente() : venta.getCotizacion().getMontoTotal();

            if (saldo > 0) {
                if (indiceFila == selectedRow) {
                    ventaSeleccionada = venta;
                    break;
                }
                indiceFila++;
            }
        }

        if (ventaSeleccionada == null) {
            JOptionPane.showMessageDialog(this, "Error al identificar la venta.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        double monto = Double.parseDouble(montoTexto);
        MetodoPago metodo = (MetodoPago) metodoComboBox.getSelectedItem();

        Pago pagoExistente = ctrlPagos.buscarPagoPorVenta(ventaSeleccionada);
        boolean exito;

        if (pagoExistente == null) {
            exito = ctrlPagos.registrarPago(ventaSeleccionada, metodo, monto);
        } else {
            exito = ctrlPagos.registrarPagoParcial(ventaSeleccionada, monto);
        }

        if (exito) {
            JOptionPane.showMessageDialog(this, "Pago registrado exitosamente.");
            listarVentasConDeuda();
            montoField.setText("");
            jLabel8.setText("Seleccione una venta en la tabla");
        } else {
            JOptionPane.showMessageDialog(this, "Error: El monto supera la deuda pendiente.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_pagoButtonActionPerformed

