    private void listarPromociones() {
        modelo.setRowCount(0);
        ArrayList<Promocion> lista = ctrlPromociones.getPromociones();

        for (Promocion p : lista) {
            Object[] row = {
                p.getNombre(),
                p.getTipo(),
                p.getValor(),
                p.getFechaInicio(),
                p.getFechaFin()
            };
            modelo.addRow(row);
        }
    }

    private void llenarCamposDesdeTabla() {
        int fila = jTable1.getSelectedRow();
        if (fila >= 0) {
            nombreField.setText(modelo.getValueAt(fila, 0).toString());
            tipoComboBox.setSelectedItem(modelo.getValueAt(fila, 1));
            apellidoField.setText(modelo.getValueAt(fila, 2).toString());
            inicioField.setText(modelo.getValueAt(fila, 3).toString());
            finalField.setText(modelo.getValueAt(fila, 4).toString());

            nombreField.setEditable(false);
        }
    }

    private void limpiarCampos() {
        nombreField.setText("");
        tipoComboBox.setSelectedIndex(0);
        apellidoField.setText("");
        inicioField.setText("");
        finalField.setText("");

        nombreField.setEditable(true);
        jTable1.clearSelection();
    }

