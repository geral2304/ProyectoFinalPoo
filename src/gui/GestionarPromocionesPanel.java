    private void agregarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarButtonActionPerformed
        String nombre = nombreField.getText();
        String valorStr = apellidoField.getText();
        String inicio = inicioField.getText();
        String fin = finalField.getText();
        TipoDescuento tipo = (TipoDescuento) tipoComboBox.getSelectedItem();

        if (nombre.isEmpty() || valorStr.isEmpty() || inicio.isEmpty() || fin.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!valorStr.matches("\\d+(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(this, "El valor debe ser un número válido.", "Error de formato", JOptionPane.ERROR_MESSAGE);
            return;
        }

        double valor = Double.parseDouble(valorStr);

        Promocion nuevaPromocion = new Promocion(nombre, tipo, valor, inicio, fin);

        if (ctrlPromociones.crearPromocion(nuevaPromocion)) {
            listarPromociones();
            limpiarCampos();
            JOptionPane.showMessageDialog(this, "Promoción agregada correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo crear. El nombre ya existe.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_agregarButtonActionPerformed

    private void modificarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarButtonActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una promoción para modificar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String nombreOriginal = modelo.getValueAt(selectedRow, 0).toString();

        String valorStr = apellidoField.getText();
        String inicio = inicioField.getText();
        String fin = finalField.getText();
        TipoDescuento tipo = (TipoDescuento) tipoComboBox.getSelectedItem();

        if (!valorStr.matches("\\d+(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(this, "El valor debe ser un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        double valor = Double.parseDouble(valorStr);

        boolean exito = ctrlPromociones.modificarPromocion(
                nombreOriginal,
                tipo,
                valor,
                inicio,
                fin
        );

        if (exito) {
            listarPromociones();
            limpiarCampos();
            JOptionPane.showMessageDialog(this, "Promoción modificada correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo modificar la promoción.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_modificarButtonActionPerformed


