    private void agregarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarButtonActionPerformed
        String dniTexto = dniField.getText();
        String nombres = nombreField.getText();
        String paterno = aPaternoField.getText();
        String materno = aMaternoField.getText();
        String direccion = direccionField.getText();
        String telefono = telefonoField.getText();
        String correo = correoField.getText();

        if (dniTexto.isEmpty() || nombres.isEmpty() || paterno.isEmpty()
                || materno.isEmpty() || direccion.isEmpty() || telefono.isEmpty() || correo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!dniTexto.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "El DNI debe ser un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int dni = Integer.parseInt(dniTexto);

        Cliente nuevoCliente = new Cliente(dni, nombres, paterno, materno, direccion, telefono, correo);

        if (ctrlClientes.crearCliente(nuevoCliente)) {
            listarClientes();
            limpiarCampos();
            JOptionPane.showMessageDialog(this, "Cliente agregado correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo crear. El DNI ya existe.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_agregarButtonActionPerformed

    private void modificarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarButtonActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un cliente para modificar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String dniTexto = dniField.getText();

        if (!dniTexto.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "El DNI debe ser un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int dni = Integer.parseInt(dniTexto);

        boolean exito = ctrlClientes.modificarCliente(
                dni,
                nombreField.getText(),
                aPaternoField.getText(),
                aMaternoField.getText(),
                direccionField.getText(),
                telefonoField.getText(),
                correoField.getText()
        );

        if (exito) {
            listarClientes();
            limpiarCampos();
            JOptionPane.showMessageDialog(this, "Cliente modificado correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo modificar el cliente.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_modificarButtonActionPerformed


