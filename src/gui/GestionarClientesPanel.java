    private void listarClientes() {
        modelo.setRowCount(0);
        ArrayList<Cliente> lista = ctrlClientes.getClientes();

        for (Cliente c : lista) {
            Object[] row = {
                c.getDni(),
                c.getNombres(),
                c.getApellidoPaterno() + " " + c.getApellidoMaterno(),
                c.getTelefono()
            };
            modelo.addRow(row);
        }
    }

    private void llenarCamposDesdeTabla() {
        int fila = jTable1.getSelectedRow();
        if (fila >= 0) {
            int dni = Integer.parseInt(modelo.getValueAt(fila, 0).toString());

            Cliente cliente = ctrlClientes.buscarPorDNI(dni);

            if (cliente != null) {
                dniField.setText(String.valueOf(cliente.getDni()));
                nombreField.setText(cliente.getNombres());
                aPaternoField.setText(cliente.getApellidoPaterno());
                aMaternoField.setText(cliente.getApellidoMaterno());
                direccionField.setText(cliente.getDireccion());
                telefonoField.setText(cliente.getTelefono());
                correoField.setText(cliente.getCorreoElectronico());

                dniField.setEditable(false);
            }
        }
    }

    private void limpiarCampos() {
        dniField.setText("");
        nombreField.setText("");
        aPaternoField.setText("");
        aMaternoField.setText("");
        direccionField.setText("");
        telefonoField.setText("");
        correoField.setText("");

        dniField.setEditable(true);
        jTable1.clearSelection();
    }

