    private void agregarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarButtonActionPerformed
        String codigo = codigoField.getText();
        String marca = marcaField.getText();
        String modeloStr = modeloField.getText();
        String color = colorField.getText();
        String anioStr = anioField.getText();
        String precioStr = precioField.getText();

        TipoVehiculo tipo = (TipoVehiculo) tipoComboBox.getSelectedItem();
        EstadoVehiculo estado = (EstadoVehiculo) estadoComboBox.getSelectedItem();

        if (codigo.isEmpty() || marca.isEmpty() || modeloStr.isEmpty() || color.isEmpty() || anioStr.isEmpty() || precioStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!anioStr.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "El año debe ser un número entero.", "Error de formato", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!precioStr.matches("\\d+(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(this, "El precio debe ser un número válido.", "Error de formato", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int anio = Integer.parseInt(anioStr);
        double precio = Double.parseDouble(precioStr);

        Vehiculo nuevoVehiculo = new Vehiculo(codigo, marca, modeloStr, color, anio, tipo, precio, estado);

        if (ctrlVehiculos.crearVehiculo(nuevoVehiculo)) {
            listarVehiculos();
            limpiarCampos();
            JOptionPane.showMessageDialog(this, "Vehículo agregado correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo crear. El código ya existe.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_agregarButtonActionPerformed

    private void modificarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarButtonActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un vehículo para modificar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String codigoOriginal = modelo.getValueAt(selectedRow, 0).toString();

        String marca = marcaField.getText();
        String modeloV = modeloField.getText();
        String color = colorField.getText();
        String anioString = anioField.getText();
        String precioString = precioField.getText();

        TipoVehiculo tipo = (TipoVehiculo) tipoComboBox.getSelectedItem();
        EstadoVehiculo estado = (EstadoVehiculo) estadoComboBox.getSelectedItem();

        if (!anioString.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "El año debe ser un número entero.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!precioString.matches("\\d+(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(this, "El precio debe ser un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int anio = Integer.parseInt(anioString);
        double precio = Double.parseDouble(precioString);

        boolean exito = ctrlVehiculos.modificarVehiculo(
                codigoOriginal,
                marca,
                modeloV,
                color,
                anio,
                tipo,
                precio,
                estado
        );

        if (exito) {
            listarVehiculos();
            limpiarCampos();
            JOptionPane.showMessageDialog(this, "Vehículo modificado correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo modificar el vehículo.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_modificarButtonActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String codigo = (String) modelo.getValueAt(selectedRow, 0);

            int confirm = JOptionPane.showConfirmDialog(this,
                    "¿Está seguro de eliminar el vehículo " + codigo + "?",
                    "Confirmar eliminación",
                    JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                ctrlVehiculos.eliminarVehiculo(codigo);
                listarVehiculos();
                limpiarCampos();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un vehículo para eliminar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

