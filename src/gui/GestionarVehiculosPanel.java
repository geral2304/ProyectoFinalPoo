    private void llenarCamposDesdeTabla() {
        int fila = jTable1.getSelectedRow();
        if (fila >= 0) {
            codigoField.setText(modelo.getValueAt(fila, 0).toString());
            marcaField.setText(modelo.getValueAt(fila, 1).toString());
            modeloField.setText(modelo.getValueAt(fila, 2).toString());
            colorField.setText(modelo.getValueAt(fila, 3).toString());
            anioField.setText(modelo.getValueAt(fila, 4).toString());
            tipoComboBox.setSelectedItem(modelo.getValueAt(fila, 5));
            precioField.setText(modelo.getValueAt(fila, 6).toString());
            estadoComboBox.setSelectedItem(modelo.getValueAt(fila, 7));

            codigoField.setEditable(false);
        }
    }

    private void listarVehiculos() {
        modelo.setRowCount(0);
        ArrayList<Vehiculo> lista = ctrlVehiculos.getVehiculos();

        for (Vehiculo v : lista) {
            Object[] row = {
                v.getCodigoUnico(),
                v.getMarca(),
                v.getModelo(),
                v.getColor(),
                v.getAnioFabricacion(),
                v.getTipo(),
                v.getPrecioBase(),
                v.getEstado()
            };
            modelo.addRow(row);
        }
    }

